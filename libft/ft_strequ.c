#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int ctr;

	ctr = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[ctr] != '\0' || s2[ctr] != '\0')
	{
		if (s1[ctr] != s2[ctr])
			return (0);
		ctr++;
	}
	return (1);
}
