#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int ctr;

	if (!s || !f)
		return ;
	ctr = 0;
	while (s[ctr] != '\0')
	{
		f(&s[ctr]);
		ctr++;
	}
}
