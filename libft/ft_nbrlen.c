#include "libft.h"

int		ft_nbrlen(int n)
{
	int len;

	len = 1;
	if (n == 0)
		return (len);
	if (n < 0)
	{
		n *= -1;
		len++;
	}
	while (n > 0)
	{
		n /= 10;
		len++;
	}
	return (len - 1);
}
