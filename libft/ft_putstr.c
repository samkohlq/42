#include "libft.h"

void	ft_putstr(char const *s)
{
	int ctr;

	if (!s)
		return ;
	ctr = 0;
	while (s[ctr] != '\0')
	{
		ft_putchar(s[ctr]);
		ctr++;
	}
}
