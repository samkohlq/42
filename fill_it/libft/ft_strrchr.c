#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *t;

	t = 0;
	while (*s)
	{
		if (*s == c)
			t = (char*)s;
		s++;
	}
	if (!c)
		return ((char*)s);
	return (t);
}
