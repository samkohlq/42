#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *r;

	if (!(r = ft_memalloc(size + 1)))
		return (NULL);
	r[size] = 0;
	while (size-- > 0)
		r[size] = 0;
	return (r);
}
