#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int ctr;

	ctr = 0;
	while (src[ctr] != '\0')
	{
		dst[ctr] = src[ctr];
		ctr++;
	}
	dst[ctr] = '\0';
	return (dst);
}
